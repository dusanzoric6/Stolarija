    public void racunaj(){
        //        SISTEMSKI METAR
        double visinaBr = Double.parseDouble(visinaText.getText())/10;
        double sirinaDesnogBr = Double.parseDouble(sirinaDesnogText.getText())/10;
        double sirinalevogBr = Double.parseDouble(sirinaLevogText.getText())/10;
        double sistemskiMetarBr = ((3*(visinaBr)+2*(sirinaDesnogBr + sirinalevogBr)))/100;

        double rabat = Double.parseDouble(rabatText.getText());
        double izborPVCSistemskog = Util.sistemskiProzorIzMape((String) pvcProfilDropBox.getSelectedItem());
        double sistemskiMetarCena = sistemskiMetarBr * izborPVCSistemskog * (100 - rabat)/100;
        sistemskiMetarText.setText(String.format("%.2f", sistemskiMetarCena));

        //        STOK

        double stokBr = (visinaBr)/100;
        double izborPVCStokMetra = Util.stokIzMape((String) pvcProfilDropBox.getSelectedItem());
        double stokCena = stokBr * izborPVCStokMetra * (100 - rabat)/100;
        stokText.setText(String.format("%.2f", stokCena));

        //        STAKLO
        double povrsinaStaklaBr = ((sirinalevogBr+sirinaDesnogBr-33)*(visinaBr-22))/10000;
        double izborStakla = myMap.get((String) stakloDropBox.getSelectedItem());

        double stakloCena = povrsinaStaklaBr * izborStakla;
        stakloCenaText.setText(String.format("%.2f", stakloCena));

        //        OKOV

        double okovCena = CeneOkova.ceneOkovaDvokrilna((sirinaDesnogBr+sirinalevogBr), visinaBr) ;
        okovText.setText(okovCena+"");

        //        SPOJNICE

//        double spojniceCena = 2.0;
//        spojniceText.setText(spojniceCena+"");

        //        OSTALO

        double ostalo = 1.05;

        //      MATERIJAL

        double materijalCena = (sistemskiMetarCena + stokCena + okovCena + stakloCena) * ostalo;
        materijal.setText(String.format("%.2f", materijalCena));
    }

