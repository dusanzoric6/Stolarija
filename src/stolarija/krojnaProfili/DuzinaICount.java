package stolarija.krojnaProfili;


public class DuzinaICount {
    private double duzina;
    private int count;

    public DuzinaICount(double duzina, int count) {
        this.duzina = duzina;
        this.count = count;
    }

    public double getDuzina() {
        return duzina;
    }

    public void setDuzina(double duzina) {
        this.duzina = duzina;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }

  
}
